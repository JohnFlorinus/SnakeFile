async function POSTFILE() {
    var target = document.getElementById("selectfile").files[0];
    if (!target) {
      return UnexpectedResponse("You need to select a file to upload");
    }
    // server side check finns också
    if (target.size>50000000) {
      return UnexpectedResponse("Maximum file size is 50 MB");
    }
    LoadingGif.style.visibility="visible";
    const endpoint = "https://api.snakefile.com/files/upload";
    try {
      var fd = new FormData();
      // Kontrollern tar in IFormFile med file som variabelnamn i sin parameter
      fd.append("file", target);
      const response = await fetch(endpoint, {
        method:"POST",
        body:fd
    });
      if (!response.ok) {
        return UnexpectedResponse(response.status + " Error<br>You may have uploaded too many files, please wait a while.");
      }
      const resp = await response.text();
      window.location.href="https://snakefile.com/download?" + resp;
    } catch (error) {
      UnexpectedResponse("An error occurred while uploading the file<br>Try again later");
    }
}

